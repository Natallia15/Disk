public class Okt_20_4_tab_umnojenia {
    public static void main(String[] args) {
        int i;
        int j;
        int z;
        for (i = 1; i <= 10; i++) {

            for (j = 1; j <= 10; j++) {
                z = i * j;
                System.out.println(i + "   *  " + j+ " = "+z);

            }
            System.out.println("    ");
        }}

}



